package in.com.OOP;

public class Rectangle_inheritance {
	private int length;
	private int width;
	protected int getLength() {
		return length;
	}
	protected void setLength(int length) {
		this.length = length;
	}
	protected int getWidth() {
		return width;
	}
	protected void setWidth(int width) {
		this.width = width;
	}
	
	

}
